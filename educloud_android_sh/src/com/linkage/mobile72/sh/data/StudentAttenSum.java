package com.linkage.mobile72.sh.data;

public class StudentAttenSum
{
    String date;
    
    int stuCount = 0;
    
    int askLeave = 0;
    
    int leave = 0;

    /**
     * @return the date
     */
    public String getDate()
    {
        return date;
    }

    /**
     * @param date the date to set
     */
    public void setDate(String date)
    {
        this.date = date;
    }

    /**
     * @return the stuCount
     */
    public int getStuCount()
    {
        return stuCount;
    }

    /**
     * @param stuCount the stuCount to set
     */
    public void setStuCount(int stuCount)
    {
        this.stuCount = stuCount;
    }

    /**
     * @return the askLeave
     */
    public int getAskLeave()
    {
        return askLeave;
    }

    /**
     * @param askLeave the askLeave to set
     */
    public void setAskLeave(int askLeave)
    {
        this.askLeave = askLeave;
    }

    /**
     * @return the leave
     */
    public int getLeave()
    {
        return leave;
    }

    /**
     * @param leave the leave to set
     */
    public void setLeave(int leave)
    {
        this.leave = leave;
    }
    
    
    
}
